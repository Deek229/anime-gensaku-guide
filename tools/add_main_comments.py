def main():
    works = json.loads(WORKS_FILE.read_text(encoding='utf-8'))
    missing = []
    over_limit = []
    for w in works:
        wid = w['id']
        comment = MAIN_COMMENTS.get(wid)
        if not comment:
            missing.append(wid)
            continue
        if len(comment) > 500:
            over_limit.append((wid, len(comment)))
        w['main_comment'] = comment
    if missing:
        print('missing:', missing)
        sys.exit(1)
    if over_limit:
        print('over 500 chars:', over_limit)
        sys.exit(1)
    WORKS_FILE.write_text(json.dumps(works, ensure_ascii=False, indent=2), encoding='utf-8')
    print(f'updated {len(works)} works with main_comment')


if __name__ == '__main__':
    main()
